const dict: Record<Language, Record<string, string>> = { en: en as any, am };

function getValue(obj: any, key: string): string | undefined {
  const parts = key.split(".");
  let current = obj;
  for (const part of parts) {
    if (current == null) return undefined;
    current = current[part];
  }
  return typeof current === "string" ? current : undefined;
}

export function t(key: TranslationKeyNested): string {
  return getValue(dict[currentLanguage], key) ?? getValue(en, key) ?? key;
}

// React hook for reactive translations
import { useState, useEffect } from "react";

export function useTranslation() {
  const [lang, setLang] = useState<Language>(currentLanguage);

  useEffect(() => {
    const unsubscribe = subscribeLanguage(setLang);
    return unsubscribe;
  }, []);

  return { t, language: lang };
}
